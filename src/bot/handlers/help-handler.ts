import { Context } from "grammy";
import { BotDeps } from "../bot";
import { resolveUser } from "../utils";
import { formatBotEmailForCopy } from "../helpers/format-email";

export function createHelpHandler(deps: BotDeps) {
  return async (ctx: Context): Promise<void> => {
    const user = await resolveUser(ctx, deps.userService);
    if (!user) return;

    const emailBlock = formatBotEmailForCopy(deps.botEmail);

    const text =
      "📖 ИНСТРУКЦИЯ ПО ИСПОЛЬЗОВАНИЮ БОТА\n\n" +
      "▫️ Как начать\n" +
      "Можете сразу отправлять текстовые или голосовые сообщения о тратах — бот сохранит их у себя. Таблицу можно подключить позже, все сохранённые траты перенесутся туда после подключения.\n\n" +
      emailBlock +
      "\n\n" +
      "▫️ Как создать таблицу\n" +
      "1. Нажмите кнопку «Создать таблицу» в /start\n" +
      "2. Таблица откроется в Google Sheets\n\n" +
      "▫️ Как добавить бота в таблицу\n" +
      "1. Откройте вашу таблицу в Google Sheets\n" +
      "2. Нажмите «Настройки доступа» (или «Поделиться»)\n" +
      "3. В поле «Добавить пользователей» вставьте email бота (см. ниже)\n" +
      "4. Выберите роль «Редактор»\n" +
      "5. Нажмите «Готово»\n\n" +
      "▫️ Как подключить таблицу к боту\n" +
      "После того как добавили бота в таблицу, отправьте команду:\n" +
      "/link ссылка на вашу таблицу*" +
      "*Ссылку скопируйте из адресной строки браузера при открытой таблице.\n\n" +
      "▫️ Что будет с тратами до подключения таблицы\n" +
      "Бот запоминает все траты. Когда вы подключите таблицу через /link, все запомненные записи автоматически перенесутся в неё.\n\n" +
      "▫️ Команды\n" +
      "/start — главное меню\n" +
      "/link — подключить таблицу\n" +
      "/invite @username — пригласить участника (только для владельца)\n" +
      "/help — эта инструкция";

    await ctx.reply(text);
  };
}
